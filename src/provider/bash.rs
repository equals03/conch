    #[test]
    fn hoists_shared_interactive_guard_across_adjacent_blocks() {
        let ir = ResolvedIr {
            blocks: vec![
                Block {
                    block_id: "nvim".into(),
                    when: vec![Predicate {
                        negated: false,
                        atom: PredicateAtom::Interactive,
                    }],
                    requires: vec![Predicate {
                        negated: false,
                        atom: PredicateAtom::Command("nvim".into()),
                    }],
                    actions: vec![Action::SetAlias {
                        name: "vim".into(),
                        value: "nvim".into(),
                    }],
                },
                Block {
                    block_id: "bat".into(),
                    when: vec![Predicate {
                        negated: false,
                        atom: PredicateAtom::Interactive,
                    }],
                    requires: vec![Predicate {
                        negated: false,
                        atom: PredicateAtom::Command("bat".into()),
                    }],
                    actions: vec![Action::SetAlias {
                        name: "cat".into(),
                        value: "bat".into(),
                    }],
                },
            ],
        };

        let text = BashProvider.render(&ir);
        assert_eq!(text.matches("if [[ $- == *i* ]]; then").count(), 1);
        assert!(
            text.contains("    # block: nvim\n    if command -v -- 'nvim' >/dev/null 2>&1; then")
        );
        assert!(text.contains("    # block: bat\n    if command -v -- 'bat' >/dev/null 2>&1; then"));
    }

