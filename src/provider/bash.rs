//! Bash provider.
//!
//! Notes:
//! - renders `when` / `requires` as Bash guard expressions
//! - path `move_front` / `move_back` are approximated in v1 as prepend/append
//! - approximation is documented directly in generated output comments

use crate::config::EnvValue;
use crate::ir::{Action, PathOp, ResolvedIr};
use crate::predicate::{Predicate, PredicateAtom};
use crate::provider::{
    build_hoist_runs, push_indented_verbatim, HoistRun, HoistedBlock, ShellProvider,
};

#[derive(Debug, Default, Copy, Clone)]
pub struct BashProvider;

impl ShellProvider for BashProvider {
    fn render(&self, ir: &ResolvedIr) -> String {
        self.render_init(ir, false)
    }
}

impl BashProvider {
    pub fn render_init(&self, ir: &ResolvedIr, guard_enabled: bool) -> String {
        let mut out = String::new();
        out.push_str("# Generated by conch for bash.\n");

        if guard_enabled {
            out.push_str("\nif [[ -z \"${__CONCH_BASH_SOURCED:-}\" ]]; then\n");
            out.push_str("    __CONCH_SOURCED=1\n");
            out.push_str("    __CONCH_BASH_SOURCED=1\n");
            for run in build_hoist_runs(&ir.blocks) {
                render_run(&mut out, &run, "    ");
            }
            out.push_str("fi\n");
        } else {
            for run in build_hoist_runs(&ir.blocks) {
                render_run(&mut out, &run, "");
            }
        }

        out
    }
}

fn render_run(out: &mut String, run: &HoistRun<'_>, prefix: &str) {
    let outer_guard = render_predicates(&run.hoistable);
    if let Some(condition) = &outer_guard {
        out.push_str(&format!("\n{prefix}if {condition}; then\n"));
        let nested_prefix = format!("{prefix}    ");
        for hoisted in &run.blocks {
            render_hoisted_block(out, hoisted, &nested_prefix);
        }
        out.push_str(&format!("{prefix}fi\n"));
    } else {
        for hoisted in &run.blocks {
            render_hoisted_block(out, hoisted, prefix);
        }
    }
}

fn render_hoisted_block(out: &mut String, hoisted: &HoistedBlock<'_>, prefix: &str) {
    let block = hoisted.block;
    out.push_str(&format!("\n{prefix}# block: {}\n", block.block_id));

    let guard = render_predicate_lists(&hoisted.residual_when, &hoisted.residual_requires);
    let indent = if guard.is_some() {
        format!("{prefix}    ")
    } else {
        prefix.to_string()
    };
    if let Some(condition) = &guard {
        out.push_str(&format!("{prefix}if {condition}; then\n"));
    }
    for action in &block.actions {
        render_action(out, action, &indent);
    }
    if guard.is_some() {
        out.push_str(&format!("{prefix}fi\n"));
    }
}

#[derive(Debug, Clone, PartialEq, Eq)]
enum GuardPart {
    Constant(bool),
    Expr(String),
}

fn render_predicate_lists(when: &[Predicate], requires: &[Predicate]) -> Option<String> {
    let mut predicates = Vec::with_capacity(when.len() + requires.len());
    predicates.extend_from_slice(when);
    predicates.extend_from_slice(requires);
    render_predicates(&predicates)
}

fn render_predicates(predicates: &[Predicate]) -> Option<String> {
    let mut expressions = Vec::new();

    for part in predicates.iter().map(render_predicate) {
        match part {
            GuardPart::Constant(true) => {}
            GuardPart::Constant(false) => return Some("false".into()),
            GuardPart::Expr(expr) => expressions.push(expr),
        }
    }

    if expressions.is_empty() {
        None
    } else {
        Some(expressions.join(" && "))
    }
}

fn render_predicate(predicate: &Predicate) -> GuardPart {
    match render_atom(&predicate.atom) {
        GuardPart::Constant(value) => {
            GuardPart::Constant(if predicate.negated { !value } else { value })
        }
        GuardPart::Expr(expr) => {
            if predicate.negated {
                GuardPart::Expr(format!("! ( {expr} )"))
            } else {
                GuardPart::Expr(expr)
            }
        }
    }
}

fn render_atom(atom: &PredicateAtom) -> GuardPart {
    match atom {
        PredicateAtom::Interactive => GuardPart::Expr("[[ $- == *i* ]]".into()),
        PredicateAtom::Login => GuardPart::Expr("shopt -q login_shell".into()),
        PredicateAtom::Shell(name) => GuardPart::Constant(name.eq_ignore_ascii_case("bash")),
        PredicateAtom::Command(name) => GuardPart::Expr(format!(
            "command -v -- {} >/dev/null 2>&1",
            bash_single_quote(name)
        )),
        PredicateAtom::EnvExists(name) => {
            GuardPart::Expr(format!("[[ -n \"${{{name}:-}}\" ]]", name = name))
        }
        PredicateAtom::EnvEquals { name, value } => GuardPart::Expr(format!(
            "[[ \"${{{name}:-}}\" == {value} ]]",
            name = name,
            value = bash_double_quote(value)
        )),
        PredicateAtom::File(path) => GuardPart::Expr(format!("[[ -e {} ]]", shell_path(path))),
        PredicateAtom::Dir(path) => GuardPart::Expr(format!("[[ -d {} ]]", shell_path(path))),
        PredicateAtom::Os(name) => GuardPart::Expr(format!(
            "[[ \"$(uname -s | tr '[:upper:]' '[:lower:]')\" == {} ]]",
            bash_double_quote(name)
        )),
        PredicateAtom::Hostname(name) => GuardPart::Expr(format!(
            "[[ \"$(hostname)\" == {} ]]",
            bash_double_quote(name)
        )),
    }
}

fn render_action(out: &mut String, action: &Action, prefix: &str) {
    match action {
        Action::SetEnv { key, value } => {
            out.push_str(prefix);
            out.push_str(&format!("export {}={}\n", key, bash_env_value(value)));
        }
        Action::SetAlias { name, value } => {
            out.push_str(prefix);
            out.push_str(&format!("alias {}={}\n", name, bash_single_quote(value)));
        }
        Action::Path(op) => match op {
            PathOp::Prepend(path) => {
                out.push_str(prefix);
                out.push_str(&format!("export PATH={}:\"$PATH\"\n", shell_path(path)));
            }
            PathOp::Append(path) => {
                out.push_str(prefix);
                out.push_str(&format!("export PATH=\"$PATH\":{}\n", shell_path(path)));
            }
            PathOp::MoveFront(path) => {
                out.push_str(prefix);
                out.push_str("# conch: move_front approximated as prepend in bash\n");
                out.push_str(prefix);
                out.push_str(&format!("export PATH={}:\"$PATH\"\n", shell_path(path)));
            }
            PathOp::MoveBack(path) => {
                out.push_str(prefix);
                out.push_str("# conch: move_back approximated as append in bash\n");
                out.push_str(prefix);
                out.push_str(&format!("export PATH=\"$PATH\":{}\n", shell_path(path)));
            }
        },
        Action::SourceLines { lines } => {
            for entry in lines {
                push_indented_verbatim(out, entry, prefix);
            }
        }
    }
}

fn bash_env_value(value: &EnvValue) -> String {
    match value {
        EnvValue::Raw(raw) => raw.clone(),
        _ => bash_double_quote(&value.as_string()),
    }
}

fn bash_single_quote(value: &str) -> String {
    format!("'{}'", value.replace('\'', "'\"'\"'"))
}

fn bash_double_quote(value: &str) -> String {
    format!(
        "\"{}\"",
        value
            .replace('\\', "\\\\")
            .replace('"', "\\\"")
            .replace('$', "\\$")
            .replace('`', "\\`")
            .replace('!', "\\!")
    )
}

fn shell_path(value: &str) -> String {
    let normalised = super::normalise_home(value);
    bash_path_quote(&normalised)
}

fn bash_path_quote(value: &str) -> String {
    format!(
        "\"{}\"",
        value
            .replace('\\', "\\\\")
            .replace('"', "\\\"")
            .replace('`', "\\`")
            .replace('!', "\\!")
    )
}

