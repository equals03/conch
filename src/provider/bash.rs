    #[test]
    fn renders_bash_ir_with_guards() {
        let ir = ResolvedIr {
            blocks: vec![Block {
                block_id: "nvim".into(),
                when: vec![Predicate {
                    negated: false,
                    atom: PredicateAtom::Interactive,
                }],
                requires: vec![Predicate {
                    negated: false,
                    atom: PredicateAtom::Command("nvim".into()),
                }],
                actions: vec![
                    Action::SetEnv {
                        key: "EDITOR".into(),
                        value: "nvim".into(),
                    },
                    Action::SetAlias {
                        name: "vim".into(),
                        value: "nvim".into(),
                    },
                    Action::Path(PathOp::Append("~/.local/bin".into())),
                ],
            }],
        };

        let text = BashProvider.render(&ir);
        assert!(text.contains("if [[ $- == *i* ]]; then\n\n    # block: nvim\n    if command -v -- 'nvim' >/dev/null 2>&1; then"));
        assert!(text.contains("export EDITOR=\"nvim\""));
        assert!(text.contains("alias vim='nvim'"));
        assert!(text.contains("export PATH=\"$PATH\":\"$HOME/.local/bin\""));
    }

