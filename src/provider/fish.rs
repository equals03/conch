//! Fish provider.
//!
//! Notes:
//! - renders `when` / `requires` as Fish guard expressions
//! - uses `fish_add_path` for path mutations
//! - supports precise `move_front` / `move_back` via `fish_add_path --move`

use crate::config::{EnvValue, SourceEntry};
use crate::ir::{Action, PathOp, ResolvedIr};
use crate::predicate::{Predicate, PredicateAtom};
use crate::provider::{
    build_hoist_runs, push_indented_verbatim, source_command_for_shell, subst, HoistRun,
    HoistedBlock, ShellProvider,
};

#[derive(Debug, Default, Copy, Clone)]
pub struct FishProvider;

impl ShellProvider for FishProvider {
    fn render(&self, ir: &ResolvedIr) -> String {
        self.render_init(ir, false)
    }
}

impl FishProvider {
    pub fn render_init(&self, ir: &ResolvedIr, guard_enabled: bool) -> String {
        let mut out = String::new();
        out.push_str("# Generated by conch for fish.\n");

        if guard_enabled {
            out.push_str("\nif not set -q __CONCH_FISH_SOURCED\n");
            out.push_str("    set -g __CONCH_SOURCED 1\n");
            out.push_str("    set -g __CONCH_FISH_SOURCED 1\n");
            for run in build_hoist_runs(&ir.blocks) {
                render_run(&mut out, &run, "    ");
            }
            out.push_str("end\n");
        } else {
            for run in build_hoist_runs(&ir.blocks) {
                render_run(&mut out, &run, "");
            }
        }

        out
    }
}

fn render_run(out: &mut String, run: &HoistRun<'_>, prefix: &str) {
    let outer_guard = render_predicates(&run.hoistable);
    if let Some(condition) = &outer_guard {
        out.push_str(&format!("\n{prefix}if {condition}\n"));
        let nested_prefix = format!("{prefix}    ");
        for hoisted in &run.blocks {
            render_hoisted_block(out, hoisted, &nested_prefix);
        }
        out.push_str(&format!("{prefix}end\n"));
    } else {
        for hoisted in &run.blocks {
            render_hoisted_block(out, hoisted, prefix);
        }
    }
}

fn render_hoisted_block(out: &mut String, hoisted: &HoistedBlock<'_>, prefix: &str) {
    let block = hoisted.block;
    out.push_str(&format!("\n{prefix}# block: {}\n", block.block_id));

    let guard = render_predicate_lists(&hoisted.residual_when, &hoisted.residual_requires);
    let indent = if guard.is_some() {
        format!("{prefix}    ")
    } else {
        prefix.to_string()
    };
    if let Some(condition) = &guard {
        out.push_str(&format!("{prefix}if {condition}\n"));
    }
    for action in &block.actions {
        render_action(out, action, &indent);
    }
    if guard.is_some() {
        out.push_str(&format!("{prefix}end\n"));
    }
}

#[derive(Debug, Clone, PartialEq, Eq)]
enum GuardPart {
    Constant(bool),
    Expr(String),
}

fn render_predicate_lists(when: &[Predicate], requires: &[Predicate]) -> Option<String> {
    let mut predicates = Vec::with_capacity(when.len() + requires.len());
    predicates.extend_from_slice(when);
    predicates.extend_from_slice(requires);
    render_predicates(&predicates)
}

fn render_predicates(predicates: &[Predicate]) -> Option<String> {
    let mut expressions = Vec::new();

    for part in predicates.iter().map(render_predicate) {
        match part {
            GuardPart::Constant(true) => {}
            GuardPart::Constant(false) => return Some("false".into()),
            GuardPart::Expr(expr) => expressions.push(expr),
        }
    }

    match expressions.len() {
        0 => None,
        1 => expressions.into_iter().next(),
        _ => Some(format!("begin; {}; end", expressions.join("; and "))),
    }
}

fn render_predicate(predicate: &Predicate) -> GuardPart {
    match render_atom(&predicate.atom) {
        GuardPart::Constant(value) => {
            GuardPart::Constant(if predicate.negated { !value } else { value })
        }
        GuardPart::Expr(expr) => {
            if predicate.negated {
                GuardPart::Expr(format!("not {expr}"))
            } else {
                GuardPart::Expr(expr)
            }
        }
    }
}

fn render_atom(atom: &PredicateAtom) -> GuardPart {
    match atom {
        PredicateAtom::Interactive => GuardPart::Expr("status is-interactive".into()),
        PredicateAtom::Login => GuardPart::Expr("status is-login".into()),
        PredicateAtom::Shell(name) => GuardPart::Constant(name.eq_ignore_ascii_case("fish")),
        PredicateAtom::Command(name) => {
            GuardPart::Expr(format!("command -q -- {}", fish_string(name)))
        }
        PredicateAtom::EnvExists(name) => GuardPart::Expr(format!(
            "begin; set -q {name}; and test -n \"${name}\"; end",
            name = name
        )),
        PredicateAtom::EnvEquals { name, value } => GuardPart::Expr(format!(
            "begin; set -q {name}; and test \"${name}\" = {value}; end",
            name = name,
            value = fish_string(value)
        )),
        PredicateAtom::File(path) => GuardPart::Expr(format!("test -e {}", shell_path(path))),
        PredicateAtom::Dir(path) => GuardPart::Expr(format!("test -d {}", shell_path(path))),
        PredicateAtom::Os(name) => GuardPart::Expr(format!(
            "test (uname -s | string lower) = {}",
            fish_string(name)
        )),
        PredicateAtom::Hostname(name) => {
            GuardPart::Expr(format!("test (hostname) = {}", fish_string(name)))
        }
    }
}

fn render_action(out: &mut String, action: &Action, prefix: &str) {
    match action {
        Action::SetEnv { key, value } => {
            out.push_str(prefix);
            out.push_str(&format!("set -gx {} {};\n", key, fish_env_value(value)));
        }
        Action::SetAlias { name, value } => {
            out.push_str(prefix);
            out.push_str(&format!("alias {}={};\n", name, fish_string(value)));
        }
        Action::Path(op) => match op {
            PathOp::Prepend(path) => {
                out.push_str(prefix);
                out.push_str(&format!("fish_add_path --prepend {};\n", shell_path(path)));
            }
            PathOp::Append(path) => {
                out.push_str(prefix);
                out.push_str(&format!("fish_add_path --append {};\n", shell_path(path)));
            }
            PathOp::MoveFront(path) => {
                out.push_str(prefix);
                out.push_str(&format!(
                    "fish_add_path --move --prepend {};\n",
                    shell_path(path)
                ));
            }
            PathOp::MoveBack(path) => {
                out.push_str(prefix);
                out.push_str(&format!(
                    "fish_add_path --move --append {};\n",
                    shell_path(path)
                ));
            }
        },
        Action::Source(source) => match source {
            SourceEntry::File(path) => {
                out.push_str(prefix);
                out.push_str(&format!("source {}\n", shell_path(path)));
            }
            SourceEntry::Command(command) => {
                let rendered = render_source_command(command);
                out.push_str(prefix);
                out.push_str(&format!("{rendered} | source\n"));
            }
        },
        Action::SourceLines { lines } => {
            for entry in lines {
                push_indented_verbatim(out, entry, prefix);
            }
        }
    }
}

fn render_source_command(command: &[String]) -> String {
    source_command_for_shell(command, "fish")
        .iter()
        .map(|part| fish_command_arg(part))
        .collect::<Vec<_>>()
        .join(" ")
}

fn fish_command_arg(value: &str) -> String {
    let segs = subst::parse_interpolated_value(value)
        .expect("interpolated command arguments must have been validated when loading config");
    if segs.len() == 1 {
        if let subst::InterpSegment::Lit(s) = &segs[0] {
            return fish_string(s);
        }
    }
    subst::fish_render_interpolated(&segs)
}

fn fish_env_value(value: &EnvValue) -> String {
    match value {
        EnvValue::String(value) => {
            let segs = subst::parse_interpolated_value(value)
                .expect("interpolated env values must have been validated when loading config");
            subst::fish_render_interpolated(&segs)
        }
        EnvValue::Bool(value) => value.to_string(),
        EnvValue::Integer(value) => value.clone(),
        EnvValue::Raw(value) => value.clone(),
    }
}

fn fish_string(value: &str) -> String {
    let escaped = value
        .replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('$', "\\$")
        .replace('(', "\\(")
        .replace(')', "\\)");
    format!("\"{escaped}\"")
}

fn shell_path(value: &str) -> String {
    let segs = subst::parse_interpolated_value(value)
        .expect("interpolated paths must have been validated when loading config");
    subst::fish_render_path_interpolated(&segs)
}

