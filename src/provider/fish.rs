    #[test]
    fn renders_fish_ir_with_guards() {
        let ir = ResolvedIr {
            blocks: vec![Block {
                block_id: "nvim".into(),
                when: vec![Predicate {
                    negated: false,
                    atom: PredicateAtom::Interactive,
                }],
                requires: vec![Predicate {
                    negated: false,
                    atom: PredicateAtom::Command("nvim".into()),
                }],
                actions: vec![
                    Action::SetEnv {
                        key: "EDITOR".into(),
                        value: "nvim".into(),
                    },
                    Action::SetAlias {
                        name: "vim".into(),
                        value: "nvim".into(),
                    },
                    Action::Path(PathOp::Prepend("~/.local/bin".into())),
                ],
            }],
        };

        let text = FishProvider.render(&ir);
        assert!(text.contains(
            "if status is-interactive\n\n    # block: nvim\n    if command -q -- \"nvim\"",
        ));
        assert!(text.contains("set -gx EDITOR \"nvim\";"));
        assert!(text.contains("alias vim=\"nvim\";"));
        assert!(text.contains("fish_add_path --prepend \"$HOME/.local/bin\";"));
    }

