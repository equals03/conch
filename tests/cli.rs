use std::process::Command;

#[test]
fn binary_inits_fish_output() {
    let output = Command::new(env!("CARGO_BIN_EXE_conch"))
        .args([
            "init",
            "fish",
            "--config",
            "tests/fixtures/configs/simple-single.toml",
        ])
        .output()
        .expect("failed to run conch binary");

    assert!(output.status.success());
    let stdout = String::from_utf8(output.stdout).unwrap();
    assert!(stdout.contains("# Generated by conch for fish."));
    assert!(stdout.contains("# block: simple"));
}

#[test]
fn check_can_target_a_single_shell() {
    let output = Command::new(env!("CARGO_BIN_EXE_conch"))
        .args(["check", "fish", "--config", "examples/conch.toml"])
        .output()
        .expect("failed to run conch binary");

    assert!(output.status.success());
}

